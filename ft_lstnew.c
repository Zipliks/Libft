#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*buf;

	buf = (t_list *)malloc(sizeof(t_list));
	if (buf == (void *)0)
		return ((void *)0);
	buf->content = content;
	buf->next = (void *)0;
	return (buf);
}
